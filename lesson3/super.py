def task5():
    """
    Задание 5

    Напишите программу, которая принимает любое целое число.
    Программа должна вывести все палиндромы, которые лежат на промежутке от заданного числа.

    Палиндром – число, которое в обе стороны читается одинаково, например 88 или 101.

    """

    def is_palindrome(array):
        size = len(array)
        if size == 1:
            return False
        for index in range(size // 2):
            if array[index] != array[-1 - index]:
                return False
        return True

    number = int(input())
    for i in range(number + 1):
        analytic_array = str(i)
        if is_palindrome(analytic_array):
            print(analytic_array, end=" ")


