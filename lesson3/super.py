def task2():
    """
    Задание 2

    Напишите программу, которая принимает от пользователя одно целое число – его возраст.
    Программа должна вывести возрастную категорию в соответствие с возрастной классификацией ВОЗ:
    0-17 - Детский возраст
    18-44 - Молодой возраст
    45-59 - Средний возраст
    60-74 - Пожилой возраст
    75-90 - Старческий возраст
    90+ - Долгожители
    """
    age_ranges_classification = [
        (range(0, 18), "Детский"),
        (range(18, 45), "Молодой"),
        (range(45, 60), "Средний"),
        (range(60, 75), "Пожилой"),
        (range(75, 90), "Старческий")
    ]
    age = int(input())
    if age > 90:
        print("Долгожители")
        return
    for intersection in age_ranges_classification:
        if age in intersection[0]:
            ans = intersection[1] + " возраст"
            print(ans)
            return
    print(f'Возраст {age} не смогли найти в классификации')


